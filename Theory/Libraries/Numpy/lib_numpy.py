import numpy as np


array_1 = np.array([6,10,11,1])

array_2 = np.array([5,9,10,4])


print(array_1)
print(type(array_1))




addition_result = array_1 + array_2

mutiplication_result = array_1 * array_2

subtraction_result = array_1 - array_2 

division_result = array_1 / array_2

#square 

square_result = array_1 ** 2


print(addition_result)
print(mutiplication_result)
print(subtraction_result)
print(division_result)
print(square_result)


#Square Root

result_sqrt = np.sqrt(array_1)

#Summation

result_sum = np.sum(array_1)

#Mean(Average)

result_mean = np.mean(array_1)


print(result_sqrt)
print(result_sum)
print(result_mean)


result_max = np.max(array_1)
result_min = np.min(array_1)


print(result_max)
print(result_min)







